// MATHEMATICAL FUNCTIONS
// Add
function add(a,b) {
    return a + b;
}
// Subtract
function subtract(a,b) {
    return a - b;
}
// Multiply
function multiple(a,b) {
    return a * b;
}
// Divide
function divide(a,b) {
    return a / b;
}
// Operate
function operate(operator,a,b) {
    operator(a,b);
}